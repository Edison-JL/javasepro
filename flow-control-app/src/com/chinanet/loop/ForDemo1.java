package com.chinanet.loop;

public class ForDemo1 {
    public static void main(String[] args) {
        //学会使用for循环、
        for (int i = 0; i < 3; i++){
            System.out.println(i);
        }
        int sum = 0;
        for (int i = 0; i <= 5; i++){
            sum += i;
        }
        System.out.println(sum);
    }


}
