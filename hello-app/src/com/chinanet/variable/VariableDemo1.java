package com.chinanet.variable;

public class VariableDemo1 {
    public static void main(String[] args) {
        //使用变量
        double money = 6.0;
        System.out.println(money);
        //从右往左执行
        money = money + 4.0;
        System.out.println(money);
        int age = 21;
        System.out.println(age);
        age = 25;
        System.out.println(age);
    }
    }

